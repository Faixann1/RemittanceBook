    private void deletePersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletePersonActionPerformed
        // TODO add your handling code here:
        int r=JOptionPane.showConfirmDialog(null, "Do you want delete the person",
                "confirmation window", JOptionPane.YES_NO_OPTION);
         if(r==JOptionPane.YES_OPTION){
             
        int ri=table.getSelectedRow();
        if(ri!=-1){
          int sid =(int)table.getValueAt(ri, 0);
        try{
            database.DbConnect.st1.executeUpdate("delete from persons_info where id="+sid);   
            JOptionPane.showMessageDialog(null, " person Removed Successfully!");
            getEntriesGiven();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
         }
         }
    }//GEN-LAST:event_deletePersonActionPerformed

