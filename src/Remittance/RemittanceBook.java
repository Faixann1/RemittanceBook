    private void displayEvent(){
        try{
            eventList.removeAllItems();
          ResultSet rs= database.DbConnect.st1.executeQuery("select * from event_info");
          while(rs.next()){
              eventList.addItem(rs.getString("event"));
          }
            
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }
     private void getEntries(){
        try{
            title.setText("RECIEVED REMITTANCE");
           
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel) table.getModel();
            int rc = dtm.getRowCount();
             while(rc--!=0){
               dtm.removeRow(0);
           }
           
               ResultSet rs=database.DbConnect.st1.executeQuery("select * from persons_info where type='recieved'");
               
            while(rs.next()){
              //String str= rs.getInt("amount")+" recieved";
                Object o[]={rs.getInt("id"),rs.getString("name"),rs.getString("father"),rs.getString("adress"),
                    rs.getInt("amount"),rs.getString("event"),rs.getString("gift")};
                dtm.addRow(o);
            }
        }
        
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
            
        }
    }
     private void getEntriesGiven(){
        try{
            title.setText("GIVEN REMITTANCE");
           
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel) table.getModel();
            int rc = dtm.getRowCount();
             while(rc--!=0){
               dtm.removeRow(0);
           }
           
               ResultSet rs=database.DbConnect.st1.executeQuery("select * from persons_info where type='given'");
               
            while(rs.next()){
                Object o[]={rs.getInt("id"),rs.getString("name"),rs.getString("father"),rs.getString("adress"),
                    rs.getInt("amount"),rs.getString("event"),rs.getString("gift")};
                dtm.addRow(o);
            }
        }
        
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
            
        }
    }

